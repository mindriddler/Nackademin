
'#Calculate area of rectangle'


def rec_calc(a, b):
    area = a * b
    return area


'# Calculate area of triangle'


def tri_calc(a, b):
    area = 0.5 * a * b
    return area


'# Return true if value is above 2'


def return_value_above_2(x):
    if x > 2:
        return True
    else:
        return False


'# Return "hello firstname lastname"'


def return_string(firstname, lastname):
    return f"hello {firstname} {lastname}"


'# return the sum'


def return_sum(x, y, z):
    return x + y + z


'# return product'


def return_product(x, y, z):
    return x * y * z
